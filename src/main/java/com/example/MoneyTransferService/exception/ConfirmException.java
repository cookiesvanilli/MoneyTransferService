package com.example.MoneyTransferService.exception;

public class ConfirmException {
    public ConfirmException() {
    }

    public ConfirmException(String message) {
        super();
    }

    public ConfirmException(String message, Throwable cause) {
        super();
    }

    public ConfirmException(Throwable cause) {
        super();
    }

    public ConfirmException(String message, Throwable cause, boolean enableSuppression,
                            boolean writableStackTrace) {
        super();
    }
}
